"""Regenerates weather/calibration_params.py from the latest backtest data.

For each city, fits both a flat (single) bias and a per-month bias on the
first 70% of days chronologically, evaluates both on the held-out last 30%,
and keeps whichever has the better Brier score — empirically, not by
assumption. Confirmed 2026-07-18 this isn't a uniform win for monthly: NYC,
Chicago, and Denver benefit (NYC dramatically — its forecast bias flips sign
between winter and summer), but Philadelphia/Austin/Miami's seasonal
variation is modest enough that the added estimation noise from smaller
per-month samples outweighs the benefit, so flat wins for them. See
kalshi-backtest-findings memory for the full numbers and methodology.

Run this periodically as more settled days accumulate — the flat-vs-monthly
choice per city, and the fitted values either way, are a snapshot from
whatever data existed when this last ran, not a permanent fact.

Usage: uv run scripts/fit_calibration_params.py
"""

from __future__ import annotations

import json
from datetime import date, timedelta

from dotenv import load_dotenv

from backtest.cache import cached_collect_rows
from backtest.calibration import brier_score
from backtest.harness import fit_empirical_normal, fit_monthly_bias, split_by_date
from kalshi_client import KalshiClient
from monitoring import track_run
from weather.probability import bracket_probability
from weather.stations import STATIONS

START_DATE = "2024-10-01"
END_DATE = (date.today() - timedelta(days=1)).isoformat()
MIN_MONTHLY_SAMPLES = 10

_OUTPUT_PATH = "weather/calibration_params.py"

_HEADER = '''"""Per-city bias and spread corrections, generated by
scripts/fit_calibration_params.py from a backtest run on {fit_date}
({start_date} to {end_date}).

DO NOT hand-edit the CALIBRATION dict below — re-run the generator instead,
so the flat-vs-monthly choice and the fitted numbers stay tied to an actual
validated comparison rather than drifting out of sync. See
kalshi-backtest-findings memory for the full methodology and per-city
results this is derived from: for each city, a flat bias and a per-month
bias are both fit on the first 70% of days chronologically and evaluated on
the held-out last 30% — whichever has the better Brier score is used. Not a
uniform win for monthly: NYC/Chicago/Denver benefit (NYC dramatically — its
forecast bias flips sign between winter and summer), but
Philadelphia/Austin/Miami's seasonal variation is modest enough that the
added estimation noise from smaller per-month samples outweighs the
benefit, so flat wins for them.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class CalibrationParams:
    overall_bias: float  # add to the live ensemble mean; positive = forecast runs cold
    monthly_bias: dict[int, float] | None  # None if flat bias validated better for this city
    std: float  # replaces the live ensemble's own std, which runs wide
    fit_date: str
    fit_days: int

    def bias_for_month(self, month: int) -> float:
        if self.monthly_bias is None:
            return self.overall_bias
        return self.monthly_bias.get(month, self.overall_bias)


CALIBRATION: dict[str, CalibrationParams] = {{
{entries}}}
'''

_FOOTER = '''

def get_calibration(series_ticker: str) -> CalibrationParams:
    try:
        return CALIBRATION[series_ticker]
    except KeyError:
        raise KeyError(
            f"No fitted calibration for {series_ticker!r}. Run "
            "scripts/fit_calibration_params.py and re-generate this file before "
            "using this series -- don't fall back to an unvalidated default for "
            "a city that hasn't actually been backtested."
        ) from None
'''


def fit_for_city(client: KalshiClient, series_ticker: str) -> dict:
    rows = cached_collect_rows(client, series_ticker, START_DATE, END_DATE, lead_days=1)
    fit_rows, eval_rows = split_by_date(rows, fit_fraction=0.7)

    overall_bias, std = fit_empirical_normal(fit_rows)
    monthly_bias = fit_monthly_bias(fit_rows, min_samples=MIN_MONTHLY_SAMPLES)

    flat_preds: list[float] = []
    monthly_preds: list[float] = []
    outcomes: list[bool] = []
    for row in eval_rows:
        month = int(row.target_date.split("-")[1])
        flat_preds.append(
            bracket_probability(row.forecast_mean + overall_bias, std, row.floor_strike, row.cap_strike)
        )
        m_bias = monthly_bias.get(month, overall_bias)
        monthly_preds.append(
            bracket_probability(row.forecast_mean + m_bias, std, row.floor_strike, row.cap_strike)
        )
        outcomes.append(row.actual_outcome)

    flat_brier = brier_score(flat_preds, outcomes)
    monthly_brier = brier_score(monthly_preds, outcomes)
    use_monthly = monthly_brier < flat_brier

    print(
        f"  {STATIONS[series_ticker].city:14s} flat={flat_brier:.4f}  monthly={monthly_brier:.4f}"
        f"  -> using {'monthly' if use_monthly else 'flat'}"
    )

    return dict(
        series_ticker=series_ticker,
        overall_bias=round(overall_bias, 4),
        monthly_bias={m: round(b, 4) for m, b in monthly_bias.items()} if use_monthly else None,
        std=round(std, 4),
        fit_days=len({row.target_date for row in fit_rows}),
        flat_brier=round(flat_brier, 4),
        monthly_brier=round(monthly_brier, 4),
        use_monthly=use_monthly,
    )


def render_entry(result: dict, fit_date: str) -> str:
    return (
        f'    "{result["series_ticker"]}": CalibrationParams(\n'
        f"        overall_bias={result['overall_bias']},\n"
        f"        monthly_bias={result['monthly_bias']!r},\n"
        f"        std={result['std']},\n"
        f'        fit_date="{fit_date}",\n'
        f"        fit_days={result['fit_days']},\n"
        f"    ),\n"
    )


def main() -> None:
    load_dotenv()
    fit_date = date.today().isoformat()

    print(f"Fitting calibration params from {START_DATE} to {END_DATE}...")
    with track_run("fit_calibration_params") as run, KalshiClient() as client:
        results = [fit_for_city(client, series_ticker) for series_ticker in STATIONS]

        entries = "".join(render_entry(r, fit_date) for r in results)
        content = (
            _HEADER.format(fit_date=fit_date, start_date=START_DATE, end_date=END_DATE, entries=entries)
            + _FOOTER
        )
        with open(_OUTPUT_PATH, "w") as f:
            f.write(content)
        print(f"\nWrote {_OUTPUT_PATH}")

        # Structured, not prose, so the dashboard's /backtest page can render
        # this directly instead of a human having to re-run the script and
        # read stdout to see which cities currently use monthly vs. flat.
        # Keyed by series_ticker, not just city: every city now has both a
        # High and a Low Temperature series (added 2026-07-19), so "city"
        # alone can't distinguish the two rows a single city produces here —
        # a city-only key silently let one metric's Brier scores overwrite
        # the other's on dashboard/app.py's read side.
        run.summary = f"Fit {len(results)} cities from {START_DATE} to {END_DATE}"
        run.detail = json.dumps(
            [
                {
                    "city": STATIONS[r["series_ticker"]].city,
                    "series_ticker": r["series_ticker"],
                    "flat_brier": r["flat_brier"],
                    "monthly_brier": r["monthly_brier"],
                    "using": "monthly" if r["use_monthly"] else "flat",
                    "fit_days": r["fit_days"],
                }
                for r in results
            ]
        )


if __name__ == "__main__":
    main()
