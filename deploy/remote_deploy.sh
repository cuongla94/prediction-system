#!/usr/bin/env bash
# Real deploy logic. Run as root on the droplet by /usr/local/bin/kalshi-deploy
# AFTER that wrapper has already validated the requested commit and moved the
# working tree to it. Not meant to be run directly — it assumes the tree is
# already at the commit being deployed.
#
# Version-controlled on purpose, unlike the wrapper: how we deploy should get
# reviewed like any other change. The wrapper stays tiny and stable on the box
# precisely so this file can change freely without touching the droplet.
#
# Ordering here is deliberate and is the main safety property: nothing stops or
# restarts a service until the new dependencies have installed successfully. A
# failure anywhere before the restart step leaves both services running their
# previous in-memory code, which is the least-bad failure mode available
# without an atomic symlink-swap deploy.
set -euo pipefail

APP_DIR="/opt/kalshi-prediction-market"
APP_USER="kalshi"
UV="/home/${APP_USER}/.local/bin/uv"
SERVICES=(kalshi-dashboard kalshi-price-feed)
SETTLE_SECONDS=10
SHA="${1:-unknown}"

cd "$APP_DIR"

log() { printf '==> %s\n' "$*"; }
fail() { printf 'DEPLOY FAILED: %s\n' "$*" >&2; exit 1; }

log "Deploying ${SHA}"

# --- Preflight -------------------------------------------------------------
# These are untracked and must already exist. A deploy is not the moment to
# discover they were wiped — check before touching anything that's running.
[ -f "${APP_DIR}/.env" ]  || fail ".env missing — refusing to restart services"
[ -d "${APP_DIR}/.venv" ] || fail ".venv missing — run deploy/setup.sh first"
[ -x "$UV" ]              || fail "uv not found at ${UV}"

# --- Ownership -------------------------------------------------------------
# git just rewrote the working tree as root; hand it back to the app user.
#
# .git is deliberately excluded and stays root-owned 0700. A $APP_USER-writable
# .git would be a straightforward privilege escalation: that account could set
# filter.*.smudge or core.fsmonitor in .git/config, both of which git executes,
# and the wrapper runs git as root on every deploy.
log "Restoring ownership (excluding .git)"
find "$APP_DIR" -mindepth 1 -maxdepth 1 ! -name .git -print0 \
  | xargs -0 -r chown -R "${APP_USER}:${APP_USER}"

# git never touches .env (it's gitignored), but re-asserting the mode is cheap
# and this file holds the Kalshi key path plus Supabase credentials.
chmod 600 "${APP_DIR}/.env"

# --- Dependencies ----------------------------------------------------------
# --frozen: uv.lock was already validated against pyproject.toml in CI with
#   --locked, so re-resolving here is wasted work — and it keeps the droplet
#   from mutating a tracked file that the next reset --hard would revert.
# --no-dev: pytest/ruff have no business in production; uv prunes them.
#
# The `sudo -u ... -H bash -c` shape with an absolute uv path is required, not
# stylistic: $APP_USER's shell is /usr/sbin/nologin and it does not get
# ~/.local/bin on PATH under sudo. See scheduler/run_pipeline.sh's own comment.
log "Syncing dependencies"
sudo -u "$APP_USER" -H bash -c "cd '${APP_DIR}' && '${UV}' sync --frozen --no-dev" \
  || fail "uv sync failed — services left running the previous code"

# --- Crontab ---------------------------------------------------------------
# Reinstall only when it actually differs. Compared against what's installed
# rather than diffed against git, which is idempotent, works on first run, and
# also catches manual drift on the box.
log "Checking crontab"
rendered="$(mktemp)"
installed="$(mktemp)"
trap 'rm -f "$rendered" "$installed"' EXIT

sed "s|/path/to/kalshi-prediction-market|${APP_DIR}|g" \
  "${APP_DIR}/scheduler/crontab.example" > "$rendered"

# `crontab -l` exits 1 when the user has no crontab at all; without `|| true`
# that kills the deploy under `set -e` on a freshly built box.
crontab -u "$APP_USER" -l > "$installed" 2>/dev/null || true

# Normalized compare (non-comment, non-blank, whitespace-collapsed) rather than
# a byte compare: Debian's cron writes a 3-line "DO NOT EDIT THIS FILE" header
# into the spool, and whether `crontab -l` strips it is version-dependent — a
# byte compare would reinstall the crontab on every single deploy.
normalize() { grep -Ev '^[[:space:]]*(#|$)' "$1" | sed 's/[[:space:]]\+/ /g' || true; }

if [ "$(normalize "$rendered")" = "$(normalize "$installed")" ]; then
  log "Crontab unchanged"
else
  log "Crontab differs — installing"
  crontab -u "$APP_USER" "$rendered" || fail "crontab install failed"
fi

# --- Systemd units ---------------------------------------------------------
# setup.sh installs these units on first provision, but later tracked changes
# must also reach /etc/systemd/system. Without this step a deploy can restart a
# healthy service under a stale unit definition and report a false success.
units_changed=0
for unit in "${SERVICES[@]}"; do
  source_unit="${APP_DIR}/deploy/${unit}.service"
  installed_unit="/etc/systemd/system/${unit}.service"
  [ -f "$source_unit" ] || fail "tracked unit missing: ${source_unit}"
  if cmp -s "$source_unit" "$installed_unit"; then
    log "${unit}.service unchanged"
  else
    log "Installing changed ${unit}.service"
    install -o root -g root -m 0644 "$source_unit" "$installed_unit"
    units_changed=1
  fi
done
if [ "$units_changed" -eq 1 ]; then
  log "Reloading systemd unit definitions"
  systemctl daemon-reload || fail "systemctl daemon-reload failed"
fi

# --- Restart ---------------------------------------------------------------
# The two units are independent (a gunicorn HTTP server vs. a WebSocket
# subscriber writing to Redis), so restarting them together is safe — the
# dashboard falls back to REST while the feed's cache is briefly stale.
#
# systemctl restart returns non-zero if either unit fails, but under `set -e`
# that would abort before the diagnostics below ever run. Capture it instead
# and keep going so the journal output actually reaches the CI log.
log "Restarting services"
restart_rc=0
systemctl restart "${SERVICES[@]}" || restart_rc=$?

# Snapshot each unit's activation timestamp BEFORE settling.
#
# `systemctl is-active` immediately after a restart is a false green: both
# units are Type=simple, so systemd reports "active" the instant it execs —
# before gunicorn binds its socket or the price feed connects. Worse, the
# price feed is Restart=always, so a crashlooping unit keeps oscillating back
# through "active" and would pass a naive check indefinitely. If this
# timestamp ADVANCES during the settle window, the unit died and was
# auto-restarted, which is exactly the case a plain is-active check misses.
declare -A started_at
for unit in "${SERVICES[@]}"; do
  started_at["$unit"]="$(systemctl show -p ActiveEnterTimestampMonotonic --value "$unit" || echo 0)"
done

log "Waiting ${SETTLE_SECONDS}s for services to settle"
sleep "$SETTLE_SECONDS"

failed=0
for unit in "${SERVICES[@]}"; do
  state="$(systemctl is-active "$unit" || true)"
  now="$(systemctl show -p ActiveEnterTimestampMonotonic --value "$unit" || echo 0)"
  unit_bad=0

  if [ "$state" != "active" ]; then
    printf 'FAIL: %s is "%s"\n' "$unit" "$state" >&2
    unit_bad=1
  elif [ "$now" != "${started_at[$unit]}" ]; then
    printf 'FAIL: %s restarted during the settle window (crashloop)\n' "$unit" >&2
    unit_bad=1
  else
    log "${unit}: active and stable"
  fi

  if [ "$unit_bad" -ne 0 ]; then
    failed=1
    printf -- '--- journalctl -u %s -n 40 ---\n' "$unit" >&2
    journalctl -u "$unit" -n 40 --no-pager >&2 || true
  fi
done

# Prove gunicorn is actually serving rather than merely running. Any HTTP
# status counts as healthy — a 302 to /login just means the PASSCODES gate is
# enabled, which is a working app.
if ! curl -sS -o /dev/null --max-time 10 http://127.0.0.1:8000/; then
  echo "FAIL: dashboard not answering on 127.0.0.1:8000" >&2
  journalctl -u kalshi-dashboard -n 40 --no-pager >&2 || true
  failed=1
fi

[ "$restart_rc" -eq 0 ] || fail "systemctl restart returned ${restart_rc}"
[ "$failed" -eq 0 ]     || fail "post-deploy verification failed"

log "Deploy of ${SHA} complete"
